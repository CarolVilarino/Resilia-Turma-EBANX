//COM WHILE

function mastigarComWhile() {
  var numeroDaMastigada = 1;

  while (numeroDaMastigada <= 25) {
    console.log(`Mastigada numero ${numeroDaMastigada}`);
    numeroDaMastigada++;
  }
  console.log("Engoliu a comida");
}

mastigarComWhile();
